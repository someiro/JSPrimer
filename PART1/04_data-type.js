
// データ型
/*
  - プリミティブ型 (基本型)
    = 真偽値 Boolean型 ... true / false のデータ型
    = 数値 Number型 ... 42, 3.14159 などの数値のデータ型
    = 巨大な整数 BigInt型 ... 9007199254740992nなどの任意精度の整数のデータ型
    = 文字列型 String型 ... "JavaScript" などの文字列のデータ型
    = undefined型 ... 値が未定義であることを意味するデータ型

  - オブジェクト (複合型)
    = プリミティブ型以外のデータ型
    = オブジェクト、配列、関数、クラス、正規表現、Dateなど
*/


